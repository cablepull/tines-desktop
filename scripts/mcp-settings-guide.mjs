import fs from 'fs/promises';
import path from 'path';
import { Client } from '@modelcontextprotocol/sdk/client/index.js';
import { StdioClientTransport } from '@modelcontextprotocol/sdk/client/stdio.js';

const assetsDir = path.resolve('docs/guides/assets');
const guidePath = path.resolve('docs/guides/settings-page-guide.md');

function getTextContent(result) {
  return (result.content || [])
    .filter((item) => item.type === 'text')
    .map((item) => item.text)
    .join('\n');
}

function getImageContent(result) {
  return (result.content || []).find((item) => item.type === 'image');
}

async function saveScreenshot(result, filename) {
  const image = getImageContent(result);
  if (!image?.data) {
    throw new Error(`Missing screenshot image payload for ${filename}`);
  }
  const target = path.join(assetsDir, filename);
  await fs.writeFile(target, Buffer.from(image.data, 'base64'));
  return target;
}

async function sleep(client, ms) {
  await client.callTool({
    name: 'send_command_to_electron',
    arguments: {
      command: 'eval',
      args: {
        code: `new Promise(resolve => setTimeout(() => resolve("slept ${ms}"), ${ms}))`,
      },
    },
  });
}

async function main() {
  await fs.mkdir(assetsDir, { recursive: true });

  const client = new Client(
    { name: 'codex-mcp-script', version: '1.0.0' },
    { capabilities: {} }
  );

  const transport = new StdioClientTransport({
    command: 'npm',
    args: ['run', 'mcp:start'],
    cwd: process.cwd(),
    env: { ...process.env, SECURITY_LEVEL: 'balanced' },
  });

  try {
    await client.connect(transport);

    const tools = await client.listTools();
    const toolNames = (tools.tools || []).map((tool) => tool.name);

    const windowInfo = await client.callTool({
      name: 'get_electron_window_info',
      arguments: {},
    });

    const loginShot = await client.callTool({
      name: 'take_screenshot',
      arguments: {},
    });
    await saveScreenshot(loginShot, 'settings-guide-01-login.png');

    await client.callTool({
      name: 'send_command_to_electron',
      arguments: {
        command: 'fill_input',
        args: { placeholder: 'e.g. Production Env', value: 'Guide Demo' },
      },
    });
    await client.callTool({
      name: 'send_command_to_electron',
      arguments: {
        command: 'fill_input',
        args: { placeholder: 'e.g. your-tenant.tines.com', value: 'example.tines.com' },
      },
    });
    await client.callTool({
      name: 'send_command_to_electron',
      arguments: {
        command: 'fill_input',
        args: { placeholder: 'sk_...', value: 'sk_demo_settings_guide' },
      },
    });
    await client.callTool({
      name: 'send_command_to_electron',
      arguments: {
        command: 'click_by_text',
        args: { text: 'Save & Connect' },
      },
    });
    await sleep(client, 900);

    await client.callTool({
      name: 'send_command_to_electron',
      arguments: {
        command: 'click_by_text',
        args: { text: 'Settings' },
      },
    });
    await sleep(client, 1500);

    const overviewShot = await client.callTool({
      name: 'take_screenshot',
      arguments: {},
    });
    await saveScreenshot(overviewShot, 'settings-guide-02-overview.png');

    await client.callTool({
      name: 'send_command_to_electron',
      arguments: {
        command: 'click_by_text',
        args: { text: 'LOCKED' },
      },
    });
    await sleep(client, 700);

    const labsShot = await client.callTool({
      name: 'take_screenshot',
      arguments: {},
    });
    await saveScreenshot(labsShot, 'settings-guide-03-labs.png');

    const markdown = `# Settings Page Guide

This guide was generated by driving the running Electron app through \`@iflow-mcp/electron-mcp-server\`.

## Tooling Check

- MCP tools discovered: ${toolNames.join(', ')}
- Electron window discovery completed through \`get_electron_window_info\`

## 1. Login Shell

The app opens on the local login shell. For this walkthrough, a temporary profile was filled so the shell could move into the main application and reach the Settings page.

![Login shell](./assets/settings-guide-01-login.png)

## 2. Settings Overview

Open **Settings** from the left sidebar. The page includes:

- tenant connection summary
- teams list
- credentials list
- the experimental labs area at the bottom

![Settings overview](./assets/settings-guide-02-overview.png)

## 3. Experimental Labs

In the **Danger Zone**, the labs toggle reveals the internal testing controls. This section is where the app exposes the chaos-story scaffolding and trigger workflow.

![Experimental labs](./assets/settings-guide-03-labs.png)

## Notes

- Teams and credentials depend on valid Tines API access.
- The labs toggle is immediate UI state; the scaffold and trigger actions then call the tenant API.
- The current Settings subtitle is: \`Tenant configuration and credential management\`.

## Raw MCP Window Info

\`\`\`text
${getTextContent(windowInfo)}
\`\`\`
`;

    await fs.writeFile(guidePath, markdown);
    console.log(`Wrote guide to ${guidePath}`);
  } finally {
    await client.close();
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
